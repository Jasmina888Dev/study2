for (let i = 0; i < 304; i++) {
  let box = document.createElement("div");
  box.setAttribute("class", "box");
  board.append(box);

  box.addEventListener("mouseover", () => setColor(box));

  box.addEventListener("mouseout", () => removeColor(box));
}

function setColor(blok) {
  let color = randomColor();
  blok.style.background = color;
  blok.style.boxShadow = `1px 1px 20px ${color}`;
}

function removeColor(blok) {
  blok.style.background = "#777474";
  blok.style.boxShadow = "";
}

function randomColor() {
  let idx = Math.floor(Math.random() * colorNames.length);
  return colorNames[idx];
}

